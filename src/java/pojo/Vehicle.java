package pojo;


public class Vehicle {
    public int vehicle_id;
    public int vehicle_cat_id;
    public int vehicle_no;
    public String vehicle_driver;
    public String vehicle_model;
    public String vehicle_company;
    public String vehicle_status;

    /**
     * @return the vehicle_id
     */
    public int getVehicle_id() {
        return vehicle_id;
    }

    /**
     * @param vehicle_id the vehicle_id to set
     */
    public void setVehicle_id(int vehicle_id) {
        this.vehicle_id = vehicle_id;
    }

    /**
     * @return the vehicle_cat_id
     */
    public int getVehicle_cat_id() {
        return vehicle_cat_id;
    }

    /**
     * @param vehicle_cat_id the vehicle_cat_id to set
     */
    public void setVehicle_cat_id(int vehicle_cat_id) {
        this.vehicle_cat_id = vehicle_cat_id;
    }

    /**
     * @return the vehicle_no
     */
    public int getVehicle_no() {
        return vehicle_no;
    }

    /**
     * @param vehicle_no the vehicle_no to set
     */
    public void setVehicle_no(int vehicle_no) {
        this.vehicle_no = vehicle_no;
    }

    /**
     * @return the vehicle_driver
     */
    public String getVehicle_driver() {
        return vehicle_driver;
    }

    /**
     * @param vehicle_driver the vehicle_driver to set
     */
    public void setVehicle_driver(String vehicle_driver) {
        this.vehicle_driver = vehicle_driver;
    }

    /**
     * @return the vehicle_model
     */
    public String getVehicle_model() {
        return vehicle_model;
    }

    /**
     * @param vehicle_model the vehicle_model to set
     */
    public void setVehicle_model(String vehicle_model) {
        this.vehicle_model = vehicle_model;
    }

    /**
     * @return the vehicle_company
     */
    public String getVehicle_company() {
        return vehicle_company;
    }

    /**
     * @param vehicle_company the vehicle_company to set
     */
    public void setVehicle_company(String vehicle_company) {
        this.vehicle_company = vehicle_company;
    }

    /**
     * @return the vehicle_status
     */
    public String getVehicle_status() {
        return vehicle_status;
    }

    /**
     * @param vehicle_status the vehicle_status to set
     */
    public void setVehicle_status(String vehicle_status) {
        this.vehicle_status = vehicle_status;
    }
    
}
